package Beginner.Step0.PatternLec1;

public class Pattern1 {
    public static void main(String[] args){
        int row = 4;
        int col = 6;
        for(int i = 0; i < row; i++){
            for(int j =0; j < col; j++){
                System.out.print(0);
            }
            System.out.println();
        }

    }
}
