package Beginner.Step0.PatternLec1;

public class Pattern2 {
    public static void main(String [] args){
        int n = 4;
        for(int i = 0; i < n; i++){
            for(int j = 0; j <= i; j++){
                System.out.print(0);
            }
            System.out.println();
        }
    }
}
